package JavaDataTypes;
/*
Non-primitive data types are called reference types because they refer to objects.
Non-primitive types typically starts with an uppercase letter (like String).
Non-primitive types can be null.
Examples of non-primitive types are Strings, Arrays, Classes etc.
 */
public class Strings {
    public static void main(String[] args) {

        String message = "Welcome to Java programming.";
        System.out.println("You have one message: " + message);
    }
}