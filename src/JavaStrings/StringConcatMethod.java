// You can also use the concat() method to concatenate strings.

package JavaStrings;

public class StringConcatMethod {
    public static void main(String[] args) {

        String first_name = "Steve";
        String last_name = "Rogers";
        String full_name = first_name.concat(last_name);

        System.out.println("Full name: "+full_name);
    }
}