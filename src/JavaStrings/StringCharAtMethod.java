// You can use the charAt() method to access a character at a specific position in a string.

package JavaStrings;

public class StringCharAtMethod {
    public static void main(String[] args) {

        String name = "Tony Stark";
        System.out.println("Character at 0th index position: "+name.charAt(0));
        System.out.println("Character at 5th index position: "+name.charAt(5));
    }
}